	<h1>User Story</h1>
	<p>Joshua wants to base his booking decisions on album reviews and articles that he reads on Pitchfork</p>
	<h2>User Case</h2>
	<p><strong>End Result: </strong>To find new acts using information from Pitchfork.</p>
	<p><strong>Scenario: </strong>Joshua is a 26 year old talent booker living in Albuquerque, New Mexico. His job is to find musical acts to book for his venue to bring in revenue. Joshua really enjoys his job but has trouble trying finding acts that have good reputation and quality music. Struggling to find ways to find this information, one of Joshua's friends suggested that he could use Pitchfork to find articles and album reviews. Joshua uses the site and decides that this will help making booking decisions in the near future.</p>
	<p><strong>Name of user and their role: </strong>Joshua; To determine booking decisions using sources on Pitchfork</p>
	<h4>Interaction Flow: </h4>
	<ul>
		<li>Joshua navigates to the address bar, types in pitchfork.com, and hits enter.</li>
		<li>Browser displays Pitchfork's homepage.</li>
		<li>Joshua see's an article he's interested in, so he click's on it.</li>
		<li>The server processes his request and navigates him to his selected page.</li>
		<li>After reading the article, Joshua searches the artist he read about by navigating to the search bar on Pitchfork.</li>
		<li>Joshua finds album reviews and news articles about the selected artist.</li>
	</ul>
